class Food {
  constructor(
    id,
    name,
    price,
    description,
    allergens,
    imagePreview,
    images,
    ingredients,
    food_beverage,
    offset,
    scale,
    order
  ) {
    this.id = id;
    this.name = name;
    this.price = price;
    this.description = description;
    this.allergens = allergens;
    this.imagePreview = imagePreview;
    this.images = images;
    this.ingredients = ingredients;
    this.food_beverage = food_beverage;
    this.offset = offset;
    this.scale = scale;
    this.order = order;
  }
}
